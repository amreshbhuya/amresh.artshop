// Function to handle search functionality
function searchArtworks() {
    const searchInput = document.getElementById('searchInput').value.trim().toLowerCase();
    
    // Clear previous search results
    const galleryContainer = document.getElementById('galleryContainer');
    galleryContainer.innerHTML = '';

    if (!searchInput) {
        // If search input is empty, display all artworks
        displayArtworks();
        return;
    }

    const filteredArtworks = artworks.filter(artwork => artwork.name.toLowerCase().includes(searchInput));
    
    if (filteredArtworks.length === 0) {
        // If no artworks match the search criteria, display a message
        const messageElement = document.createElement('p');
        messageElement.textContent = 'No artworks found matching your search.';
        galleryContainer.appendChild(messageElement);
        return;
    }
    
    // Display filtered artworks
    filteredArtworks.forEach(artwork => {
        const artworkElement = document.createElement('div');
        artworkElement.classList.add('artwork');
        artworkElement.innerHTML = `
            <img src="${artwork.image}" alt="${artwork.name}">
            <div class="artwork-details">
                <h3 class="artwork-name">${artwork.name}</h3>
                <p class="artwork-price">${artwork.price}</p>
                <p class="artwork-category">${artwork.category}</p>
                <button class="add-to-cart-btn" onclick="addToCart('${artwork.name}', '${artwork.price}', '${artwork.image}')">Add to Cart ðŸ›’</button>
            </div>
        `;
        galleryContainer.appendChild(artworkElement);
    });
}


